import re

def compress_font_data(c_code):
    # 分割输入为行
    lines = c_code.splitlines()
    
    # 处理第一行（数组声明）和最后一行（闭合花括号）
    output_lines = [lines[0]]  # 保留数组声明行
    
    current_char_data = []  # 存储当前字符的所有字节
    
    for line in lines[1:-1]:  # 跳过第一行和最后一行
        stripped = line.strip()
        
        # 检测空行（字符分隔符）
        if not stripped:
            if current_char_data:  # 如果已收集字符数据
                # 将当前字符的所有字节合并为一行
                compressed_line = "    " + ", ".join(current_char_data) + ","
                output_lines.append(compressed_line)
                output_lines.append("")  # 添加空行作为分隔符
                current_char_data = []
            continue
        
        # 提取十六进制字节值
        bytes_in_line = re.findall(r'0x[0-9A-Fa-f]{2}', stripped)
        if bytes_in_line:
            current_char_data.extend(bytes_in_line)
    
    # 处理最后一个字符
    if current_char_data:
        compressed_line = "    " + ", ".join(current_char_data) + ","
        output_lines.append(compressed_line)
    
    # 添加闭合花括号
    output_lines.append(lines[-1])
    
    return "\n".join(output_lines)

